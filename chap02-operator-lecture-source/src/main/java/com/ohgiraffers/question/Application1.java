package com.ohgiraffers.question;

public class Application1 {

    public int sumTwoNumbers(int num1, int num2) {
        return num1 + num2;
    }

    public int minusTwoNumber(int num1, int num2){
        return num1 - num2;
    }

    public int multiTwoNumbers(int num1, int num2){
        return num1 * num2;
    }

    public int divideTwoNumbers(int num1, int num2){
        return num1 / num2;
    }


}

